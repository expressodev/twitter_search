function do_fetch($file){

		if ( ! extension_loaded('curl') || ! function_exists('curl_init'))
		{
			return "plugin no curl support";
		}	
		//$file = rawurlencode($file);
	
		$ch = curl_init();
		curl_setopt ($ch, CURLOPT_URL, $file);
		curl_setopt($ch, CURLOPT_RETURNTRANSFER, 1);
		curl_setopt($ch, CURLOPT_CONNECTTIMEOUT, 0);
		curl_setopt($ch, CURLOPT_TIMEOUT, 30);
		
		$file_contents = curl_exec($ch);

		// check curl errors
		$info = curl_getinfo($ch);
			$contents = "";
			$contents = $file_contents;	

		curl_close($ch);
		
		//print_r($contents);
		
		
		
		$contents = str_replace("foo(", " ", $contents);
		$contents = str_replace('"})', '"}', $contents);
		
		
		
		return $contents;  
} 
    



function doLoop($tagdata,$fdata){
global $LOC, $TMPL, $FNS, $DSP, $SESS, $PREFS;

			if(!$fdata)
			{
			return $tagdata;
			//return $TMPL->no_results(); 
			}

    		$convert_url = ( ! $TMPL->fetch_param('convert_url')) ? '' : $TMPL->fetch_param('convert_url');
			$word_censoring = ( ! $TMPL->fetch_param('word_censoring')) ? '' : $TMPL->fetch_param('word_censoring');

			
				if ( ! class_exists('Typography'))
				{
					require PATH_CORE.'core.typography'.EXT;
				}
				$TYPE = new Typography;
				$TYPE->convert_curly = FALSE;
				$TYPE->text_format = "none";
				$TYPE->parse_smileys = FALSE;
				$TYPE->allow_img_url = 'n';
				
				if($convert_url == "yes"){
					$TYPE->auto_links = 'y';
				}else{
					$TYPE->auto_links = 'n';
				}
				if($word_censoring == "off"){
					$TYPE->word_censor = FALSE;
				}else{
					$TYPE->word_censor = TRUE;
				}
				
			
			
				
		$dimsum = "";
		$count = 0;	
		foreach($fdata as $row) {

			$the_date = "";
			$my_date = "";
			$count++;
			$tagdata = $TMPL->tagdata;
			$tagdata = $FNS->prep_conditionals($tagdata, $row);
			
			foreach ($TMPL->var_single as $key => $val)
			{
			
				if($key == "text"){
				$row[$val] = $TYPE->parse_type($row[$val]);
				}
				
				// do switch
				if (preg_match("/^switch\s*=.+/i", $key))
				{
					$sparam = $FNS->assign_parameters($val);
					$sw = '';
					
					if (isset($sparam['switch']))
					{
						$sopt = explode("|", $sparam['switch']);
						$sw = $sopt[($count-1 + count($sopt)) % count($sopt)];
					}
					$tagdata = $TMPL->swap_var_single($val, $sw, $tagdata);
				}



			// do date formating
				if (ereg("relative_date", $key))
				{
					$relative_date = $LOC->format_timespan(time() - strtotime($row['created_at']));
					$tagdata = $TMPL->swap_var_single($key, $relative_date, $tagdata);	
				}


				// do date formating
				if (ereg("^created_at", $key))
				{
					$the_date = strtotime($row['created_at']);
					$the_date = $LOC->set_server_offset($the_date, 1);
					$the_date = $LOC->decode_date($val, $the_date, false);
					$tagdata	= $TMPL->swap_var_single($key, $the_date, $tagdata);	
				}
								
					
			
				// do normal vars
				if (isset($row[$val]))	{
					$tagdata = $TMPL->swap_var_single($val, $row[$val], $tagdata);
				}else{
					$tagdata = $TMPL->swap_var_single($val, '', $tagdata);
				}
				
			}
			$dimsum .= $tagdata;
		}
	return $dimsum;	
}

