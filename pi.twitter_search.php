	$this->jsondata = $this->request_json($TMPL->tagparams);
	
	
	if(!$this->jsondata){
		return $TMPL->no_results();
	}	
	
	// get at the class object we want
	$this->eventdata = $this->jsondata->results;
	
	if(count($this->eventdata) == "0")
	{
		return $TMPL->no_results();
	}
	
	$i=0;
	foreach($this->eventdata as $event => $edata){

			// loop over elements and build new array for ee output
			foreach($edata as $key => $value)
			{
				$events[$i][$key] = $value;
			}
			

	$i++;
	}

	// output to template	
	$tagdata = $this->doLoop($TMPL->tagdata,$events);
	return $tagdata;
	  
    }
	


