function request_json($tagparams){
		  $arguments = "";


$skip = array('cache','refresh','word_censoring','convert_url');

	foreach ($tagparams as $key => $value)
		{
			// clean up passed vars
			$value = urlencode($value);
			
			// skip EE Specific vars
			if(!in_array($key,$skip))
			{
				$arguments .= "&".$key."=".$value;
			}
		}
		
	$json_url = $this->t_base_url."".$arguments;
	$json_content = $this->do_fetch($json_url);
		
	
	return $this->decode($json_content);
}	





